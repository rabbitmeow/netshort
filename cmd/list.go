package cmd

import (
	"fmt"
	"io/ioutil"
	"os"

	"github.com/spf13/cobra"
	"github.com/spf13/viper"
)

// listCmd represents the list command
var listCmd = &cobra.Command{
	Use:   "list",
	Short: "List all of your link",
	Long:  `Read all of your link that already created in _redirects file`,
	Run: func(cmd *cobra.Command, args []string) {
		if !checkAppDir() {
			fmt.Println("_redirect file does not exist (or is a directory)")
			os.Exit(1)
		}
		data, err := ioutil.ReadFile(viper.GetString("app.path") + "/_redirects")
		if err != nil {
			fmt.Println("Error when read _redirects file", err)
			return
		}
		fmt.Println("\n", string(data))
	},
}

